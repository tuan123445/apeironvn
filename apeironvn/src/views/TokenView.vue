      ],
      chartOptions: {
        chart: {
          height: 350,
          type: "area",
        },
        dataLabels: {
          enabled: false,
        },
        stroke: {},
        xaxis: {
          categories: [],
          title: {
            text: "Số tháng",
          },
        },
        yaxis: [
          {
            labels: {
              formatter: function (val) {
                return val;
              },
            },
            title: {
              text: "Phần Trăm(%)",
            },
          },
        ],
      },
    };
  },
  created() {
    for (var i = 0; i < 60; i++) {
      this.chartOptions.xaxis.categories.push(`${i + 1}`);
    }
    this.series[0].data = this.getTGEList(6, 8, 32, 60);
    this.series[1].data = this.getTGEList(10, 8, 32, 60);
    this.series[2].data = this.getTGEList(5, 6, 25, 60);
    this.series[3].data = this.getTGEList(4, 0, 14, 60);
    this.series[4].data = this.getTGEList(30, 0, 40, 60);
    this.series[5].data = this.getTGEList(20, 8, 32, 60);
    this.series[6].data = this.getTGEList(5, 8, 32, 60);
    this.series[7].data = this.getTGEList(6, 8, 32, 60);
    this.series[8].data = this.getTGEList(14, 8, 32, 60);

    this.scrollToTop();
  },
  methods: {
    getTGEList(percent, blockMonths, tgeMonths, allMonth) {
      let totalTokens = 1000000000;
      let totalVestingTokens = (totalTokens * percent) / 100;
      let vestingPerMonthTokens = totalVestingTokens / tgeMonths;
      let vestingPerMonthPercent = (vestingPerMonthTokens / totalTokens) * 100;
      let vestingPerMonthPercentList = [];
      for (let i = 1; i <= blockMonths; i++) {
        vestingPerMonthPercentList.push(0);
      }
      for (let y = 1; y <= tgeMonths; y++) {
        let lastValue = this.getLastValueOfArr(vestingPerMonthPercentList);
        vestingPerMonthPercentList.push(lastValue + vestingPerMonthPercent);
      }
      let leftMonth = allMonth - tgeMonths - blockMonths;
      for (let x = 1; x <= leftMonth; x++) {
        let lastValue = this.getLastValueOfArr(vestingPerMonthPercentList);
        vestingPerMonthPercentList.push(lastValue);
      }

      return vestingPerMonthPercentList;
    },
    getLastValueOfArr(arr) {
      if (arr.length != 0) {
        let arrLength = arr.length;
        return arr[arrLength - 1];
      }
      return 0;
    },
  },
};
</script>

